#include <iostream>
using namespace std;
int main()
{
      char x, y, z;
      cout << "Enter three characters: ";
      cin >> x >> y >> z;
      cout << x << " = " << int(x) << endl;
      cout << y << " = " << int(y) << endl;
      cout << z << " = " << int(z) << endl;
      return 0;
}
